using System;

namespace AdditionalTask
{
    static class Calculator
    {
        public static double PlusMethod(double a, double b)
        {
            double result = a + b;
            return result;
        }
        public static double MinusMethod(double a, double b)
        {
            double result = a - b;
            return result;
        }
        public static double MultiplyMetod(double a, double b)
        {
            double result = a * b;
            return result;
        }
        public static double DivideMethod(double a, double b)
        {
            double result = a / b;
            return result;
        }
    }
}
